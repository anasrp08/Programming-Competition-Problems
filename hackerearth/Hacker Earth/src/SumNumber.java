import java.util.*;
class SumNumber {
    public static void main(String x[])
    {
        Scanner sc=new Scanner(System.in);
        int len=sc.nextInt();
        
        while(len>0)
        {
            int a=sc.nextInt();
            int b=sc.nextInt();
            System.out.println(a+b);
            
            len--;
        }
    
    }
}

